#while döngüsü, belirli bir koşul karşılandığı sürece çalışan bir döngü türüdür.
#Yani, while döngüsü, başlangıçta belirli bir koşulu kontrol eder,koşul doğru olduğu sürece döngü içindeki kodu tekrar tekrar çalıştırır.
#Koşul yanlış olduğunda döngü sona erer.

i=0

while i <10:
    print("Merhaba")
    i=i+1    #ya da i += 1 şeklinde de yazılabilir. #i değeri her döngü adımında 1 artırılır. Bu, döngünün kaç kez çalıştığını sayar.

#Bu Python kodu, bir while döngüsü kullanarak "Merhaba" kelimesini 10 kez ekrana yazdırmayı amaçlar.



